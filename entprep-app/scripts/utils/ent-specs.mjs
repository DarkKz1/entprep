/**
 * Distributes `total` questions across sections proportionally to their weights.
 * Returns an array of { sectionId, sectionName, count, topics }.
 * Every section gets at least 1 question if weight > 0.
 *
 * @param {object} spec - A SPEC_* object with sections[].
 * @param {number} total - Total number of questions to distribute.
 * @returns {Array<{ sectionId: string, sectionName: string, count: number, topics: Array<{ num: number, name: string }> }>}
 */
export function distributeBySpec(spec, total) {
  const sections = spec.sections;

  // First pass: proportional allocation (min 1 per section with weight > 0)
  const alloc = sections.map(s => ({
    section: s,
    count: Math.max(1, Math.round(total * s.weight)),
  }));

  // Adjust to match total exactly
  let sum = alloc.reduce((acc, r) => acc + r.count, 0);
  let diff = total - sum;

  // Sort by count descending for adjustment — add to largest, remove from largest
  const sorted = [...alloc].sort((a, b) => b.count - a.count);
  let idx = 0;
  const maxIter = total * 2; // safety limit
  while (diff !== 0 && idx < maxIter) {
    const target = sorted[idx % sorted.length];
    if (diff > 0) {
      target.count++;
      diff--;
    } else if (target.count > 1) {
      target.count--;
      diff++;
    }
    idx++;
  }

  return alloc.map(r => ({
    sectionId: r.section.id,
    sectionName: r.section.name,
    count: r.count,
    topics: r.section.topics,
  }));
}

/**
 * Builds an AI prompt context string from a section object.
 *
 * Output format:
 * ```
 * Раздел: Section Name
 * Темы:
 *   - [1] Topic description
 *   - [2] Topic description
 * ```
 *
 * @param {{ id: string, name: string, topics: Array<{ num: number, name: string }> }} section
 * @returns {string}
 */
export function buildTopicContext(section) {
  if (!section || !section.topics || section.topics.length === 0) {
    return `Раздел: ${section?.name ?? 'Неизвестный раздел'}`;
  }
  const items = section.topics
    .map(t => `  - [${t.num}] ${t.name}`)
    .join('\n');
  return `Раздел: ${section.name}\nТемы:\n${items}`;
}
